function reduceToSingle(n) {
  if (n === 11 || n === 22 || n === 33) return n;
  if (n < 10) return n;
  const sum = String(n).split('').reduce((a, c) => a + Number(c), 0);
  return reduceToSingle(sum);
}

export function calcLifePathNumber(year, month, day) {
  const m = reduceToSingle(month);
  const d = reduceToSingle(day);
  const ySum = String(year).split('').reduce((a, c) => a + Number(c), 0);
  const y = reduceToSingle(ySum);
  return reduceToSingle(m + d + y);
}

